class Bill {
    constructor(
        locationId,
        billingDate,
        complete,
        paidAt,
        total,
        entriesPerDay,
    ) {
        this.locationId = locationId
        this.billingDate = billingDate
        this.complete = complete
        this.paidAt = paidAt
        this.total = total
        this.entriesPerDay = entriesPerDay
    }

}
